//发送GT5663配置参数
//mode:0,参数不保存到flash
//     1,参数保存到flash
u8 GT5663_Send_Cfg(u8 mode)
{
	u8 buf[2];
	u8 i=0;
	buf[0]=0;
	buf[1]=mode;	//是否写入到GT5663 FLASH?  即是否掉电保存
	for(i=0;i<sizeof(GT5663_CFG_TBL);i++)buf[0]+=GT5663_CFG_TBL[i];//计算校验和
    buf[0]=(~buf[0])+1;
	GT5663_WR_Reg(GT_CFGS_REG,(u8*)GT5663_CFG_TBL,sizeof(GT5663_CFG_TBL));//发送寄存器配置
	GT5663_WR_Reg(GT_CHECK_REG,buf,2);//写入校验和,和配置更新标记
	return 0;
} 

//向GT5663写入一次数据
//reg:起始寄存器地址
//buf:数据缓缓存区
//len:写数据长度
//返回值:0,成功;1,失败.
u8 GT5663_WR_Reg(u16 reg,u8 *buf,u8 len)
{
	u8 i;
	u8 ret=0;
	CT_IIC_Start();	
 	CT_IIC_Send_Byte(GT_CMD_WR);   	//发送写命令 	 
	CT_IIC_Wait_Ack();
	CT_IIC_Send_Byte(reg>>8);   	//发送高8位地址
	CT_IIC_Wait_Ack(); 	 										  		   
	CT_IIC_Send_Byte(reg&0XFF);   	//发送低8位地址
	CT_IIC_Wait_Ack();  
	for(i=0;i<len;i++)
	{	   
    	CT_IIC_Send_Byte(buf[i]);  	//发数据
		ret=CT_IIC_Wait_Ack();
		if(ret)break;  
	}
    CT_IIC_Stop();					//产生一个停止条件	    
	return ret; 
}

//从GT5663读出一次数据
//reg:起始寄存器地址
//buf:数据缓缓存区
//len:读数据长度			  
void GT5663_RD_Reg(u16 reg,u8 *buf,u8 len)
{
	u8 i; 
 	CT_IIC_Start();	
 	CT_IIC_Send_Byte(GT_CMD_WR);   //发送写命令 	 
	CT_IIC_Wait_Ack();
 	CT_IIC_Send_Byte(reg>>8);   	//发送高8位地址
	CT_IIC_Wait_Ack(); 	 										  		   
 	CT_IIC_Send_Byte(reg&0XFF);   	//发送低8位地址
	CT_IIC_Wait_Ack();  
 	CT_IIC_Start();  	 	   
	CT_IIC_Send_Byte(GT_CMD_RD);   //发送读命令		   
	CT_IIC_Wait_Ack();	   
	for(i=0;i<len;i++)
	{	   
    	buf[i]=CT_IIC_Read_Byte(i==(len-1)?0:1); //发数据	  
	} 
    CT_IIC_Stop();//产生一个停止条件    
} 

//初始化GT5663触摸屏
//返回值:0,初始化成功;1,初始化失败 
u8 GT5663_Init(void)
{
	u8 temp[5]; 
	GPIO_InitTypeDef  GPIO_InitStructure;	
	__HAL_RCC_GPIOC_CLK_ENABLE();
	__HAL_RCC_GPIOB_CLK_ENABLE();//使能GPIOB,C时钟

	GPIO_InitStructure.Pin = T_PEN_Pin ;//PB1设置为上拉输入
	GPIO_InitStructure.Mode = GPIO_MODE_INPUT;//输入模式
	GPIO_InitStructure.Speed = GPIO_SPEED_FREQ_HIGH;//100MHz
	GPIO_InitStructure.Pull = GPIO_PULLUP;//上拉
	HAL_GPIO_Init(T_PEN_GPIO_Port, &GPIO_InitStructure);//初始化
		
	GPIO_InitStructure.Pin = T_CS_Pin;//PC13设置为推挽输出
	GPIO_InitStructure.Mode = GPIO_MODE_OUTPUT_PP;//输出模式
	HAL_GPIO_Init(T_CS_GPIO_Port, &GPIO_InitStructure);//初始化	
	
	CT_IIC_Init();      	//初始化电容屏的I2C总线  
	GT_RST_0;				//复位
	delay_ms(10);
 	GT_RST_1;				//释放复位		    
	delay_ms(10); 
	
	GPIO_InitStructure.Pin = T_PEN_Pin;//PB1设置为浮空输入
	GPIO_InitStructure.Mode = GPIO_MODE_OUTPUT_PP;//输出模式
	GPIO_InitStructure.Pull = GPIO_NOPULL;
	HAL_GPIO_Init(T_PEN_GPIO_Port, &GPIO_InitStructure);//初始化	
	
	delay_ms(100);  
	GT5663_RD_Reg(GT_PID_REG,temp,4);//读取产品ID
	temp[4]=0;
	if(strcmp((char*)temp,"5663")==0)//ID==5663
	{
		temp[0]=0X02;			
		GT5663_WR_Reg(GT_CTRL_REG,temp,1);//软复位GT5663
		GT5663_RD_Reg(GT_CFGS_REG,temp,1);//读取GT_CFGS_REG寄存器
//		if(temp[0]<0X60)//默认版本比较低,需要更新flash配置
//		{
//			printf("Default Ver:%d\r\n",temp[0]);
//			GT5663_Send_Cfg(1);//更新并保存配置
//		}
		delay_ms(10);
		temp[0]=0X00;	 
		GT5663_WR_Reg(GT_CTRL_REG,temp,1);//结束复位   
		return 0;
	} 
	return 1;
}

const u16 GT5663_TPX_TBL[5]={GT_TP1_REG,GT_TP2_REG,GT_TP3_REG,GT_TP4_REG,GT_TP5_REG};
//扫描触摸屏(采用查询方式)
//mode:0,正常扫描.
//返回值:当前触屏状态.
//1,触屏无触摸;0,触屏有触摸
u8 GT5663_Scan(u8 mode)
{
	u8 buf[4];
	u8 i=0;
	u8 res=1;
	u8 temp;
 	static u8 t=0;//控制查询间隔,从而降低CPU占用率   
	t++;
	if((t%10)==0||t<10)//空闲时,每进入10次CTP_Scan函数才检测1次,从而节省CPU使用率
	{
		GT5663_RD_Reg(GT_GSTID_REG,&mode,1);	//读取触摸点的状态 
		//printf("mode=%d\r\n",mode);		
 		if(mode&0X80&&((mode&0XF)<6))
		{
			temp=0;
			GT5663_WR_Reg(GT_GSTID_REG,&temp,1);//清标志 		
		}		
		if((mode&0XF)&&((mode&0XF)<6))
		{
			temp=0XFF<<(mode&0XF);		//将点的个数转换为1的位数,匹配tp_dev.sta定义 
			TouchData.tpsta=(~temp); 
			TouchData.lcdx[4]=TouchData.lcdx[0];	//保存触点0的数据
			TouchData.lcdy[4]=TouchData.lcdy[0];
			for(i=0;i<5;i++)
			{
				if(TouchData.tpsta&(1<<i))	//触摸有效?
				{
					GT5663_RD_Reg(GT5663_TPX_TBL[i],buf,4);	//读取XY坐标值
					if(tftlcd_data.dir==1)//横屏
					{
						TouchData.lcdy[i]=((u16)buf[1]<<8)+buf[0];
						TouchData.lcdx[i]=800-(((u16)buf[3]<<8)+buf[2]);
					}else
					{
						TouchData.lcdx[i]=((u16)buf[1]<<8)+buf[0];
						TouchData.lcdy[i]=((u16)buf[3]<<8)+buf[2];
					}  
					//printf("x[%d]:%d,y[%d]:%d\r\n",i,TouchData.lcdx[i],i,TouchData.lcdy[i]);
				}			
			} 
			res=0;
			if(TouchData.lcdx[0]>tftlcd_data.width||TouchData.lcdy[0]>tftlcd_data.height)//非法数据(坐标超出了)
			{ 
				if((mode&0XF)>1)		//有其他点有数据,则复第二个触点的数据到第一个触点.
				{
					TouchData.lcdx[0]=TouchData.lcdx[1];
					TouchData.lcdy[0]=TouchData.lcdy[1];
					t=0;				//触发一次,则会最少连续监测10次,从而提高命中率
				}else					//非法数据,则忽略此次数据(还原原来的)  
				{
					TouchData.lcdx[0]=TouchData.lcdx[4];
					TouchData.lcdy[0]=TouchData.lcdy[4];
					mode=0X80;		
				}
			}else t=0;					//触发一次,则会最少连续监测10次,从而提高命中率
		}
	}
	if((mode&0X8F)==0X80)//无触摸点按下
	{ 
		TouchData.tpsta=0;
		TouchData.lcdx[0]=0xffff;
		TouchData.lcdy[0]=0xffff;	 
	} 	
	if(t>240)t=10;//重新从10开始计数
	return res;
}
